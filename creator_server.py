def load_existing_config():
    """Load config.json if it exists. Returns a dict of pre-fill values or empty dict."""
    config_path = Path("config.json")
    if not config_path.exists():
        return {}
    try:
        with open(config_path, "r", encoding="utf-8") as f:
            config = json.load(f)
        site = config.get("site", {})
        pages = config.get("pages", [])

        # Find home page and links page
        home = next((p for p in pages if p.get("slug") == "index"), {})
        links_page = next((p for p in pages if p.get("slug") == "links"), {})

        # Flatten links from link_groups
        all_links = []
        for group in links_page.get("link_groups", []):
            all_links.extend(group.get("links", []))
        # Also handle flat links list
        all_links.extend(links_page.get("links", []))

        return {
            "site_title": site.get("title", ""),
            "site_description": site.get("description", ""),
            "theme": site.get("theme", "dark"),
            "home_intro": home.get("intro", ""),
            "home_body": " ".join(home.get("body", [])),
            "links": all_links,
        }
    except Exception:
        return {}


