def open_preview(preview_path):
    """Open the preview in a browser. Cross-platform."""
    uri = Path(preview_path).resolve().as_uri()
    try:
        if sys.platform == "win32":
            import subprocess as sp
            try:
                sp.run(["cmd", "/c", "start", "chrome", uri], check=True)
            except Exception:
                webbrowser.open(uri)
        else:
            webbrowser.open(uri)
    except Exception:
        # If browser launch fails entirely, the success page shows the path.
        pass


class Handler(BaseHTTPRequestHandler):

    def log_message(self, format, *args):
        # Suppress default request logging to keep console clean.
        pass

    def do_GET(self):
        existing = load_existing_config()
        self.respond(build_form_html(existing))

    def do_POST(self):
        length = int(self.headers.get("Content-Length", 0))
        raw = self.rfile.read(length).decode("utf-8")
        data = parse_qs(raw)

        def get(name):
            return data.get(name, [""])[0].strip()

        # Collect links — clean loop with single exit condition.
        links = []
        for i in range(1, 21):
            label = get(f"link_label_{i}")
            url = get(f"link_url_{i}")
            desc = get(f"link_desc_{i}")
            if label and url:
                links.append({
                    "label": label,
                    "url": url,
                    "description": desc,
                    "type": "Place to go"
                })

        config = {
            "site": {
                "title": get("site_title"),
                "description": get("site_description"),
                "theme": get("theme") if get("theme") in ("light", "dark") else "dark"
            },
            "pages": [
                {
                    "slug": "index",
                    "title": "Home",
                    "intro": get("home_intro"),
                    "body": [get("home_body")]
                },
                {
                    "slug": "links",
                    "title": "Where People Should Go",
                    "intro": "Creator-defined places and resources.",
                    "body": [
                        "These places are chosen by the site creator. "
                        "FYN does not maintain a directory or infer relationships between communities."
                    ],
                    "link_groups": [
                        {
                            "title": "Places to go next",
                            "links": links
                        }
                    ]
                }
            ]
        }

        with open("config.json", "w", encoding="utf-8") as f:
            json.dump(config, f, indent=2)

        try:
            subprocess.run(
                [sys.executable, "build.py"],
                check=True
            )
            preview_path = str(Path("docs") / "index.html")
            threading.Thread(
                target=open_preview,
                args=(preview_path,),
                daemon=True
            ).start()
            self.respond(SUCCESS_HTML.format(preview_path=preview_path))
        except subprocess.CalledProcessError as exc:
            self.respond(f"""
            <html>
            <body style="font-family: Georgia; padding: 2rem; background: #111; color: #eee;">
            <h1 style="color: #c9a84c;">Build failed.</h1>
            <p>Something went wrong during the build. Check the terminal for details.</p>
            <pre style="background: #181818; padding: 1rem; color: #bbb;">{exc}</pre>
            </body>
            </html>
            """)
            return

        threading.Thread(target=self.server.shutdown, daemon=True).start()

    def respond(self, html):
        self.send_response(200)
        self.send_header("Content-Type", "text/html; charset=utf-8")
        self.end_headers()
        self.wfile.write(html.encode("utf-8"))


def open_browser():
    time.sleep(1.5)
    webbrowser.open(f"http://localhost:{PORT}")


def main():
    server = HTTPServer(("localhost", PORT), Handler)
    print(f"Finding Your Neighborhood")
    print(f"Creator running at http://localhost:{PORT}")
    print(f"Opening in browser...")
    threading.Thread(target=open_browser, daemon=True).start()
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print("\nShutting down.")
        server.server_close()


if __name__ == "__main__":
    main()
